package org.example.Operators;

public class MakingDecision {
    public static void main(String[] args) {
        int value1 = 0, value2 = 3;
        if (value1 == value2) {
            System.out.println("Equal");
        }
        if(value1 != value2)
        {
            System.out.println("Not Equal");
        }
    }
}
