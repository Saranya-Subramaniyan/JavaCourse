package org.example.flowControl.Loops;

public class forLoop {
    public static void main(String[] args) {
        //print n numbers
        for (int i = 0; i < 10; i++){
            System.out.println(i);
        }
        String[] str={"saran","saru","apple","jack","swa"};
        //for each
        for(String s:str){
            System.out.println(s);
        }
    }
}